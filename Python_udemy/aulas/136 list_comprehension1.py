# é uma forma rápida de criar listas a partir de iteráveis
lista =[
    numero * 2
    for numero in range(10)
]
print(lista)
#entenda que tudo acima está na mesma linha de código e que o que está á esquerda do FOR será feito quantas vezes o range determinar.

